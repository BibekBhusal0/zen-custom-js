// Funcitons I could paste in console just for quick testing
function copyAllCommands() {
  const ids = Array.from(document.querySelectorAll("command")).map((el) => el.id);
  return navigator.clipboard.writeText(JSON.stringify(ids, null, 2));
}

function doCommand(command) {
  const commandEl = document.getElementById(command);
  if (commandEl?.doCommand) {
    commandEl.doCommand();
  } else {
    console.error(`command ${command} not found`);
  }
}

